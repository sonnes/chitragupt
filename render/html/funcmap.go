// toolIcon returns an inline SVG icon for a tool name (16x16, currentColor).
func toolIcon(name string) template.HTML {
	key := strings.ToLower(name)
	svg, ok := toolIcons[key]
	if !ok {
		svg = toolIcons["_default"]
	}
	return template.HTML(svg)
}

